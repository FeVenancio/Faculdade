import java.util.Scanner;

    public class ForDuplo {

        public static void main(String[] args) {

            int qtdEsteiras = 3;
            int qtdProdutos = 50;
            int ultimoProduto = 0;

            for(int esteiras = 1; esteiras <= qtdEsteiras; esteiras ++){
                System.out.println("Esteira " + esteiras);
                
            
            
                for(int produtos = 1; produtos <= qtdProdutos; produtos++){
                    System.out.println("Produto " + produtos);
                    produtos = ultimoProduto;

                }

                
            }





        }
    }